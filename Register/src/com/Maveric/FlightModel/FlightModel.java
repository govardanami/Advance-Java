package com.Maveric.FlightModel;

public class FlightModel {
	
private	String flightname;
private	String source;
private	String destination;
private	String fare;
private	String numberofseats;
private	String aircraftname;
private	String addflightbutton;
public String getFlightname() {
	return flightname;
}
public void setFlightname(String flightname) {
	this.flightname = flightname;
}
public String getSource() {
	return source;
}
public void setSource(String source) {
	this.source = source;
}
public String getDestination() {
	return destination;
}
public void setDestination(String destination) {
	this.destination = destination;
}
public String getFare() {
	return fare;
}
public void setFare(String fare) {
	this.fare = fare;
}
public String getNumberofseats() {
	return numberofseats;
}
public void setNumberofseats(String numberofseats) {
	this.numberofseats = numberofseats;
}
public String getAircraftname() {
	return aircraftname;
}
public void setAircraftname(String aircraftname) {
	this.aircraftname = aircraftname;
}
public String getAddflightbutton() {
	return addflightbutton;
}
public void setAddflightbutton(String addflightbutton) {
	this.addflightbutton = addflightbutton;
}


	
	
}
